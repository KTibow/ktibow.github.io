<h2>Live Pet Translation</h2>
<p>Speak raccoon, live the adventure.</p>
<p>
  We're bringing Live Pet Translation to the Translate app, beginning with support for the animal we
  all most yearn to understand: raccoons.
</p>
<p>
  Whether you're seeking a deeper sense of connection to the raccoons you adopted after a basement
  invasion, or you just want to know what's running through the minds of the chaos critters digging
  through your garbage bins, Live Pet Translation with raccoon support will open the door to mutual
  understanding - or at least a little mutual mischief.
</p>
<p>
  Translate has changed the way people communicate by quickly and easily translating text, images,
  and live conversations. Now it's time to bridge the gap across species. The next time a raccoon
  looks at you demanding to know why their cotton candy disappears in water, or whether the
  drive-thru will be serving curly fries tonight, you'll be ready - with Translate.
</p>
