<BlogHeader {date} {bg} title={`Not understanding "skill issue" is a skill issue`} />
<p>
  When I was younger, the phrase "skill issue" was raw nonsensical bait to me. I think part of why
  was because I saw skill and knowledge as the same thing - I thought you could just learn about
  anything in a few minutes.
</p>
<p>
  Now make no mistake, skill is a type of knowledge. To be a skilled thinker is to <em>know</em> how
  to think. To be skilled at basketball is to <em>know</em> how to move your body in just the right way.
  But skill isn't easily learnable - you have to do things yourself and go through RL to get skilled.
  If it was easy to transfer muscle memory or guidelines to thought, then you wouldn't see so much training
  or drilling in the world.
</p>
<p>
  "skill issue" has a real and useful meaning: "that's the fault of your own lack of experience and
  effort." I've picked it up for this reason, and I eagerly say: Crashing something that you want to
  use is a skill issue. Not finding content you'd enjoy on a platform full of it is a skill issue.
  Forgetting to renew a resource is a skill issue. <a
    href="https://ktibow.github.io/blog/slamffusion/">Turning random walks into stringy lines</a
  >, if you're not trying to get stringy lines, is also a skill issue.
</p>
