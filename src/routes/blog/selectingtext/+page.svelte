<BlogHeader {bg} title="Selecting is easy, actually" />
<p>You probably know about the simple ways to select text:</p>
<ul>
  <li>Click and drag</li>
  <li>Ctrl+a</li>
  <li>In a text editor, click and shift+click</li>
  <li>In a text editor, click and shift+up/down/left/right</li>
</ul>
<p>
  However, there are some more obscure ways. I'm posting them here because it seems generally
  knowledgeable people or LLMs often don't know about them.
</p>
<ul>
  <li>
    <em>Anywhere</em>, click and use techniques like shift+click/up/down/left/right
  </li>
  <li>In Firefox, ctrl+drag on a table</li>
  <li>
    Change your selection to a word/paragraph by double clicking or triple clicking respectively,
    and optionally drag from there
  </li>
</ul>
<p>
  You should really try out the first one. Dragging is tedious and easy to mess up compared to two
  clicks.
</p>
<p><em>2025 update:</em> Also try ctrl+dragging for multiple selections!</p>
