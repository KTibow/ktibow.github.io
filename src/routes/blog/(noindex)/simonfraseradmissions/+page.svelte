<h2>English requirement</h2>
<p>
  Complete the last 3 years of education in English with a good grade. (There are other options for
  non-native speakers but that's not what I'm going to cover here.)
</p>
<h2>Quantitative and Analytical Skills requirement</h2>
<p>
  Complete a grade 11/12 math course with a passing grade. (Technically there are other options for
  IB folks or folks in countries that use the General Certificate of Education or for transfer
  students but that's not what I'm going to cover here.)
</p>
<h2>General requirements</h2>
<p>
  The SAT/ACT requirement is optional for students applying for Fall 2024 admission onwards.
  Applicants who have a SAT or ACT are welcome to submit their test score. Applicants will not be at
  a disadvantage if they cannot or choose not to submit a test score. Applicants must also present a
  minimum of 3 senior level academic courses, including English, and submit an interim transcript
  showing final grades for all courses completed and a list of all senior courses currently in
  progress.
</p>
