  <p><strong>Sync Config Dir (experimental)</strong></p>
  <p>
    Sync config dir obsidian or not (inner folder .git and node_modules would be ignored). Please be
    aware that this may impact all your plugins' or Obsidian's settings, and may require you restart
    Obsidian after sync. Enable this at your own risk.
  </p>
