<h2>General US admission requirements</h2>
<ul>
  <li>
    Graduation from an academic or college preparation program at a US regionally accredited school.
    Applicants are encouraged, but not required, to present a minimum of six academic and
    non-academic Grade 12 courses.
  </li>
  <li>
    English (composition/literature) to the senior level. ESL English courses are not acceptable to
    satisfy English requirements for admission.
  </li>
  <li>Three years of mathematics to the junior level.</li>
  <li>A minimum grade of B- in an English course to at least the junior level.</li>
  <li>
    SAT or ACT. Applicants who have an SAT or ACT test score and would like them to be considered as
    part of the UBC admissions process are welcome to submit their test scores. Applicants who
    choose not to submit a standardized test score will not be disadvantaged. (UBC's SAT institution
    code is 0965 and its ACT institution code is 5259.)
  </li>
  <li>
    AP courses completed as part of the high school curriculum may be used to meet admission
    requirements.
  </li>
</ul>
<h2>Typical requirements</h2>
<p>Requirements vary. For some, there are no additional requirements. For others:</p>
<ul>
  <li>
    Science degree: 1 credit precalc, 1 credit chemistry, 1 credit physics (or a grade of A in
    precalc and AP chemistry) are required. Taking language arts, math, and science in your junior
    and senior year is recommended.
  </li>
  <li>
    International Economics degree: 1 credit precalc is required. Taking language arts,
    math/science, and social studies in your junior and senior year is recommended.
  </li>
  <li>
    Dental Hygiene: 2 years of biology / AP biology and 2 years of chemistry / AP chemistry are
    required. A supplemental submission and interview process are necessary. Taking language arts
    and science in your junior and senior year is recommended.
  </li>
</ul>
